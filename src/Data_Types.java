
public class Data_Types {
    public static  void main( String[] args)
    {
        int age; // declaration
        age = 10; // assignation
        System.out.println("The value of age is : " + age);
        double salary;
        salary = 22.56;
        System.out.println("The salary is : $"+ salary + "Cr");
        boolean statement = true;
        System.out.println("The statement is :"+ statement);
        char ch = 'x';
        System.out.println("My favorite character is : " +ch);
        char myGrade = 'B'; // camelCase naming convention
        System.out.println(myGrade);
        String myName = "Badhon Pain";
        System.out.println("My name is "+myName);
    }
}
